bool check(int n) {
    while(n > 0) {
        if(n%10 != 4 && n%10 != 7) return false;
        n/= 10;
    }
    return true;
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL); cout.tie(NULL);
    
    int N; cin >> N;
    for(int i = N; i >= 4; i--) {
        if(check(i)) {
            cout << i;
            break;
        }
    }
    
    return 0;
}
